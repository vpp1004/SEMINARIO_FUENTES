library(devtools)
library(greenR)
library(dplyr)
datoshuesca<-get_osm_data("Huesca,Aragón,España")
indice_verde_huesca<-calculate_green_index(datoshuesca,4326,100)
str(indice_verde_huesca)
typeof(indice_verde_huesca)
dthuesca<-as.data.frame(indice_verde_huesca)
huesca<-summarise(.data=dthuesca,areasverdes=mean(green_index_green_area,na.rm=TRUE),arboles=mean(green_index_tree,na.rm=TRUE),porcentajeverde=mean(green_index,na.rm=TRUE))
View(huesca)
str(huesca)

